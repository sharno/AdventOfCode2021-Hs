parse s = foldl f [] s
  where
  -- we keep the first illegal character while we continue folding
  f ")" _ = ")"
  f "]" _ = "]"
  f "}" _ = "}"
  f ">" _ = ">"

  -- opening braces
  f xs '(' = '(':xs
  f xs '[' = '[':xs
  f xs '{' = '{':xs
  f xs '<' = '<':xs

  -- closing braces
  f ('(':xs) ')' = xs
  f ('[':xs) ']' = xs
  f ('{':xs) '}' = xs
  f ('<':xs) '>' = xs

  -- otherwise : first illegal character
  f _ x = [x]


